#include "libft.h"

char	*ft_strdup(const char *s1)
{
	unsigned int	len;
	char			*str;
	int				i;

	i = 0;
	len = ft_strlen(s1);
	str = (char *)malloc(sizeof(char) * (len + 1));
	if (!str)
		return (0);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = 0;
	return (str);
}
