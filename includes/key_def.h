#ifndef KEY_DEF_H
# define KEY_DEF_H

/*
**	ESC
*/
# define ESC			0x35

/*
**	SHIFT
*/
# define SHIFT			0x101
# define KEY_C			0x08
# define KEY_O			0x1f

/*
**	Translate
*/
# define ARROW_LEFT		0x7b
# define ARROW_RIGHT	0x7c
# define ARROW_DOWN		0x7d
# define ARROW_UP		0x7e

# define KEY_F			0x03
# define KEY_B			0x0b

/*
**	Rotate
*/
# define KEY_X			0x07
# define KEY_Y			0x10
# define KEY_Z			0x06

# define KEY_P			0x23
# define KEY_T			0x11

/*
**	Scale
*/
# define KEY_E			0x0e
# define KEY_R			0x0f

#endif
